#include "libft.h"

size_t		ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t i;
	size_t result;

	result = 0;
	i = ft_strlen(src);
	result = i;
	i = 0;
	while (dst[i] && i < size)
		i++;
	result += (i < size) ? i : size;
	if ((int)(size - ft_strlen(dst) - 1) > 0)
		ft_strncat(dst, src, size - ft_strlen(dst) - 1);
	return (result);
}
